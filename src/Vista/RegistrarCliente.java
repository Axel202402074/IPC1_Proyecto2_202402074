    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        String dpi = txtDPI.getText();
        String nombre = txtNombre.getText();
        String usuario = txtUsuario.getText();
        String contraseña = new String(txtContraseña.getPassword());

        if (dpi.isEmpty() || nombre.isEmpty() || usuario.isEmpty() || contraseña.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.");
            return;
        }

        if (!dpi.matches("\\d{13}")) {
            JOptionPane.showMessageDialog(this, "El DPI debe tener 13 dígitos numéricos.");
            return;
        }

        ControladorRegistroCliente ctrl = ControladorRegistroCliente.getInstancia();
        if (ctrl.registrarCliente(dpi, nombre, usuario, contraseña, contraseña)) {
            JOptionPane.showMessageDialog(this, "¡Registro exitoso!");
            new Login_1().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Error: Usuario ya existe o contraseñas no coinciden.");
        }
    }//GEN-LAST:event_btnCrearActionPerformed

